    private void insertRecords() {
      
    try {
            Class.forName("java.sql.DriverManager"); //loading drivers to connect GUI with databse
            Connection c = ConnectDB.getConnect(); //getting connection with Sports_db
            Statement st = c.createStatement(); //enabling SQL statements  in java code
            
            int y = st.executeUpdate("insert into student_details values("+this.studentID + ",'" 
                    + this.firstName + "','" +this.lastName+"','"+this.grade+"','"+this.house+"','"+this.tutorName+"',"
                            + "'"+this.emailID+"','"+this.sport+"','"+this.ageGroup+"','"+this.gender+"',"
                                    +this.coachID+")"); 
            
            if (y > 0) {
                JOptionPane.showMessageDialog(this, "Congratulations! Your records are added to the database successfully"); 
               
                   LoginPage lp=new LoginPage();
                   lp.setVisible(true);
                this.dispose();
               
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    
    }
