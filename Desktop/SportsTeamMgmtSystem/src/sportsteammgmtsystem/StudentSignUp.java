    private void SSU_Sport_cbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SSU_Sport_cbActionPerformed
     
         
        if(!this.SSU_Sport_cb.getSelectedItem().toString().equals("Select")){
        try{
            
               Class.forName("java.sql.DriverManager"); //loading and registering drivers required for MYSQL
               
               Connection con=ConnectDB.getConnect();
               Statement s=con.createStatement();
                ResultSet r=s.executeQuery("select * from coach_details  where sport='"+this.SSU_Sport_cb.getSelectedItem().toString()+"'"); 
                if(r.next()){ 
                       
                    this.SSU_Coach_txt.setText(r.getString("firstname")+" "+r.getString("lastname"));
                 }
                
                 
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else
            JOptionPane.showMessageDialog(this, "Please select sport first");
        
    }//GEN-LAST:event_SSU_Sport_cbActionPerformed

