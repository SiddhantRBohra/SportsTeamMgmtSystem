public boolean validateEntries(){
    boolean flag=true;
    if(this.SSU_StudentID_txt.getText().isEmpty()){
        flag=false;
        JOptionPane.showMessageDialog(this, "Please enter your Student ID");}
        else if(this.SSU_FormTutor_txt.getText().isEmpty()){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please enter your Form Tutor");
        }  else if(this.SSU_AgeGroup_cb.getSelectedItem().toString().equals("Select")){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please select your Age Group");       
        } else if(this.SSU_House_cb.getSelectedItem().toString().equals("Select")){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please select your House");
        } else if(this.SSU_Grade_cb.getSelectedItem().toString().equals("Select")){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please select your Grade");
        } else if(!this.SSU_Male_radbtn.isSelected() && !this.SSU_Female_radbtn.isSelected()){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please Select Your Gender");}
        else if(this.SSU_Sport_cb.getSelectedItem().toString().equals("Select")){
            flag=false;
            JOptionPane.showMessageDialog(this, "Please Select Your Sport");}
    
    if(flag){
            this.studentID = Integer.parseInt(this.SSU_StudentID_txt.getText());
            this.tutorName = this.SSU_FormTutor_txt.getText();
             getCoachId();
             
             this.schoolID=003257;
             this.ageGroup=this.SSU_AgeGroup_cb.getSelectedItem().toString();
             this.house=this.SSU_House_cb.getSelectedItem().toString();
             this.grade=this.SSU_Grade_cb.getSelectedItem().toString();
             this.sport=this.SSU_Sport_cb.getSelectedItem().toString();
            if(this.SSU_Male_radbtn.isSelected())
                this.gender="Male";
            else
                this.gender="Female";
        }    
    return flag;
    }
    public void getCoachId(){
        String name=this.SSU_Coach_txt.getText();
        
       String fname=name.toString().split(" ")[0];
     String lname=name.toString().split(" ")[1];
     
     try{
            
               Class.forName("java.sql.DriverManager"); //loading and registering drivers required for MYSQL
               
               Connection con=ConnectDB.getConnect();
               Statement s=con.createStatement();
                ResultSet r=s.executeQuery("select * from coach_details  where firstname='" +fname+"'  and lastname='"+lname+"'"); 
                if(r.next()){ 
                       
                    this.coachID=r.getInt("coach_id");
                 }
                
                 
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
     
     
    }
          
